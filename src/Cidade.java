
public class Cidade {

	int id;
	String nome;
	int popoulacao;
	int area;
	int idEstado;


	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getPopoulacao() {
		return popoulacao;
	}

	public void setPopoulacao(int popoulacao) {
		this.popoulacao = popoulacao;
	}

	public int getArea() {
		return area;
	}

	public void setArea(int area) {
		this.area = area;
	}

	public int getIdEstado() {
		return idEstado;
	}

	public void setIdEstado(int idEstado) {
		this.idEstado = idEstado;
	}



}
